function convertirGradosRadianes(grados) {
    return grados * (Math.PI / 180);
}

console.log(convertirGradosRadianes(90));   // Math.PI / 2

console.log(convertirGradosRadianes(180));   // Math.PI

console.log(convertirGradosRadianes(270));
