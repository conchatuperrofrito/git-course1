// Ejercicio 1261: Usar los operadores typeof e instanceof para comprobar el tipo de dato de un objeto Date.

let fechaHoraActual = new Date();

console.log(fechaHoraActual);

console.log();

console.log(typeof fechaHoraActual);    // object

console.log();

console.log(fechaHoraActual instanceof Date);   // true
